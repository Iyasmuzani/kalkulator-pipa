const SDRs = [26, 21, 17, 13.6, 11, 9, 7.4];
let result = 'var rucikaPipes = {\n';

let keys = Object.keys(orig).map(Number).sort((a,b)=>a-b);
for (let i = 0; i < keys.length; i++) {
  let od = keys[i];
  let obj = orig[od];
  
  // calculate SDR 9 and SDR 7.4
  // ISO 4065 preferred values sometimes diverge, but let's use exact rounding
  let emin9 = Math.ceil((od / 9) * 10) / 10;
  if (emin9 < 2.0) emin9 = 2.0;
  let emin74 = Math.ceil((od / 7.4) * 10) / 10;
  if (emin74 < 2.0) emin74 = 2.0;
  
  // Actually if we look at real tables, e.g. OD 90 SDR 9: 90/9 = 10.0.
  // 110/9 = 12.222 -> 12.3.
  
  obj[9] = emin9.toFixed(1);
  obj[7.4] = emin74.toFixed(1);
  
  // fix the float values to 1 decimal place strings
  let pairs = [];
  for (let sdr of SDRs) {
    if (obj[sdr] !== undefined) {
      let val = Number(obj[sdr]).toFixed(1);
      pairs.push(sdr + ': ' + val);
    }
  }
  
  let line = '  ' + od + ': { ' + pairs.join(', ') + ' }';
  if (i < keys.length - 1) line += ',';
  result += line + '\n';
}
